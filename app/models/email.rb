    row.to_hash
    row[:bridge] = DOMAINHASH.fetch((row.to_hash[:email_domain.to_s]),0)
    #row[:email_domain] = (((row.to_hash)[:email_domain.to_s]) + "added")
    Email.create! row.to_hash
  end
end
